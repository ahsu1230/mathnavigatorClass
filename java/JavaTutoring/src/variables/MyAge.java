package variables;

public class MyAge {
	
	public static void main(String[] args) {
		String name = "Aaron";
		int age = 28;
		
		System.out.println("Hi! My name is " + name + ".");
		System.out.println("Today, I my age is: " + age);
		System.out.println("Next year, my age will be: " + (age + 1));
		System.out.println("Last year, my age was: " + (age - 1));
	}
}
